def update_excel(existing_rows, new_rows):
    """更新Excel文件"""
    wb = load_workbook(EXCEL_PATH) if os.path.exists(EXCEL_PATH) else Workbook()

    if "秋招信息汇总" in wb.sheetnames:
        ws = wb["秋招信息汇总"]
    else:
        ws = wb.active
        ws.title = "秋招信息汇总"

    # 写表头
    for col, header in enumerate(HEADERS, 1):
        ws.cell(row=1, column=col, value=header)

    # 排序：开放中优先 → 公司评分高优先 → 截止日期近优先 → 收集日期新优先
    all_rows = existing_rows + new_rows

    def sort_key(row):
        company = row[0] or ""
        industry = row[5] or ""  # 专业要求列现在存放行业信息
        note = row[11] or ""
        status = row[12] or ""
        deadline = row[8] or ""

        # 1. 状态：开放中=0, 其他=1
        status_rank = 0 if status == "开放中" else 1

        # 2. 公司评分：越高越好（取负数用于升序）
        size_text = extract_size_from_note(note)
        score = compute_company_score(company, industry, size_text)
        score_rank = -score  # 负值，score越高的排前面

        # 3. 截止日期：越近越靠前（招满为止放最后）
        try:
            dl = deadline.strip().replace(".", "-").replace("/", "-")
            dl_date = datetime.strptime(dl[:10], "%Y-%m-%d")
            deadline_rank = dl_date.toordinal()
        except (ValueError, IndexError):
            deadline_rank = 999999  # 招满为止/未知排后面

        # 4. 收集日期：越新越靠前
        try:
            cd = (row[10] or "").strip()
            cd_date = datetime.strptime(cd[:10], "%Y-%m-%d")
            collect_rank = -cd_date.toordinal()
        except (ValueError, IndexError):
            collect_rank = 0

        return (status_rank, score_rank, deadline_rank, collect_rank)

    all_rows.sort(key=sort_key)

    # 写入数据
    for row_idx, row_data in enumerate(all_rows, 2):
        for col_idx, value in enumerate(row_data, 1):
            ws.cell(row=row_idx, column=col_idx, value=value or "")

    # 调整列宽
    column_widths = [18, 40, 10, 20, 10, 14, 35, 12, 12, 15, 12, 30, 10]
    for i, width in enumerate(column_widths, 1):
        col_letter = chr(64 + i) if i <= 26 else chr(64 + (i - 1) // 26) + chr(65 + (i - 1) % 26)
        ws.column_dimensions[col_letter].width = width

    # 冻结首行
    ws.freeze_panes = "A2"

    wb.save(EXCEL_PATH)
    wb.close()
    print(f"  Excel已保存: {len(all_rows)} 条 (新增 {len(new_rows)} 条)")


