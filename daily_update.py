# 公司名称 → Tier 得分 (0-100)
def compute_company_score(company_name, industry, company_size_text):
    """计算公司评分：越高越值得投"""
    score = 0
    name = (company_name or "").strip()
    ind = (industry or "").strip()
    size = (company_size_text or "").strip()

    # 1. Tier 精确匹配 (最高优先级)
    for kw in TIER_0_COMPANIES:
        if kw in name:
            score = max(score, 100)
            break

    if score < 100:
        for kw in TIER_1_COMPANIES:
            if kw in name:
                score = max(score, 80)
                break

    # 2. Tier 2 关键词匹配
    if score < 80:
        for kw in TIER_2_KEYWORDS:
            if kw in name:
                score = max(score, 60)
                break

    # 3. 行业加分
    if score < 60:
        if is_finance_industry(ind):
            score = max(score, 50)  # 金融行业至少50分
        elif "国企" in ind or "央企" in ind or "事业单位" in ind:
            score = max(score, 45)
        elif "互联网" in ind or "科技" in ind:
            score = max(score, 40)

    # 4. 公司规模加分
    size_score = parse_company_size(size)
    if size_score > 0:
        score = max(score, min(size_score, 90))

    # 5. 兜底
    if score == 0:
        score = 20  # 未知小公司

    return score

# 从Excel行解析公司规模（备注列中可能有"规模:xxx"）
def extract_size_from_note(note):
    """从备注中提取公司规模"""
    if not note:
        return ""
    import re
    m = re.search(r'规模:(.+?)(?:;|$)', note)
    if m:
        return m.group(1).strip()
    return ""

