def deduplicate(existing_rows, new_rows):
    """按公司名称+岗位名称去重，返回新数据"""
    # 构建已有数据的key集合
    existing_keys = set()
    for row in existing_rows:
        key = (row[0] or "").strip() + "|||" + (row[1] or "").strip()[:30]
        existing_keys.add(key)

    truly_new = []
    for row in new_rows:
        key = (row[0] or "").strip() + "|||" + (row[1] or "").strip()[:30]
        if key not in existing_keys:
            truly_new.append(row)

    return truly_new


