# 公司规模 → 数值映射
def parse_company_size(size_text):
    """将公司规模文本转为数值（用于排序）"""
    if not size_text:
        return 0
    s = size_text.strip().replace(" ", "")
    # 10万以上 → 100
    if "10万" in s or "十万" in s or "100000" in s:
        return 100
    # 20000~100000 → 80
    if "20000" in s and "100000" in s:
        return 80
    # 10000~20000 → 60
    if "10000" in s and "20000" in s:
        return 60
    # 5000~20000 → 50
    if "5000" in s and "20000" in s:
        return 50
    # 5000~10000 → 40
    if "5000" in s and "10000" in s:
        return 40
    # 1000~5000 → 25
    if "1000" in s and "5000" in s:
        return 25
    # 1000以内 → 10
    if "1000以内" in s or "1000" in s:
        return 10
    # 300以内 → 5
    if "300" in s:
        return 5
    # 500以内 → 8
    if "500" in s:
        return 8
    # 未知
    return 0

