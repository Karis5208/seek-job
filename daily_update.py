def mark_expired(rows):
    """标记已截止的岗位"""
    today = datetime.now()
    updated = 0
    for row in rows:
        deadline = row[8]  # 截止时间列（索引8）
        status = row[12]   # 状态列（索引12）
        if status == "已截止":
            continue
        if deadline and deadline != "招满为止":
            # 尝试解析截止日期
            try:
                dl = deadline.strip().replace(".", "-").replace("/", "-")
                dl_date = datetime.strptime(dl[:10], "%Y-%m-%d")
                if dl_date < today:
                    row[12] = "已截止"
                    updated += 1
            except ValueError:
                pass
    if updated:
        print(f"  标记已截止: {updated} 条")
    return rows


